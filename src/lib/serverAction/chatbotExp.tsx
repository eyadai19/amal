export async function ChatbotExpAction(
    question: string,
    answer: string,
): Promise<
    | { question: string; answers: string[] }
    | { field: string; message: string }
    | { answer: string }
    | { answer: string; exception: string }  // النوع الجديد
> {
    try {
        // ========== البداية: السؤال الرئيسي ==========
        if (!question) {
            return {
                question: "ما هو الهدف؟",
                answers: ["فئة الوثائق الأساسية", "فئة الحقوق القانونية"]
            };
        }

        // ========== فئة الوثائق الأساسية ==========
        if (question === "ما هو الهدف؟" && answer === "فئة الوثائق الأساسية") {
            return {
                question: "اختر نوع الوثيقة:",
                answers: [
                    "وثائق التعليم والتدريب",
                    "وثائق السكن والعنوان",
                    "الوثائق الأساسية",
                    "وثائق الإفراج والسجل الجنائي"
                ]
            };
        }

       
        // -- وثائق العمل والتعليم ب ---
        if (question === "فئة الوثائق الأساسية" && answer === "وثائق العمل والتعليم") {
            return {
                question: "وثائق العمل والتعليم:",
                answers: ["الشهادات التعليمية", "شهادة الخبرة العملية"]
            };
        }

        if (question === "وثائق العمل والتعليم:" && answer === "الشهادات التعليمية") {
            return {
                answer: "استخراج بدل فاقد من مديرية التربية في المحافظة."
            };
        }

        if (question === "وثائق العمل والتعليم:" && answer === "شهادة الخبرة العملية") {
            return {
                answer: "تطلب من جهة العمل السابقة لك."
            };
        }


          // --- وثائق السكن والعنوان ---
          if (question === "فئة الوثائق الأساسية" && answer === "وثائق السكن والعنوان") {
            return {
                question: "وثائق السكن والعنوان:",
                answers: ["إثبات السكن"]
            };
        }

        if (question === "وثائق السكن والعنوان:" && answer === "إثبات السكن") {
            return {
                answer: "الحصول عليه من المختار أو عقد إيجار مصدق"
            };
        }
        // --- الوثائق الأساسية ---
        if (question === "فئة الوثائق الأساسية" && answer === "الوثائق الأساسية") {
            return {
                question: "الوثائق الأساسية",
                answers: ["استخراج هوية شخصية", "شهادة الميلاد"]
            };
        }

        if (question === "الوثائق الأساسية" && answer === "استخراج هوية شخصية") {
            return {
                answer: "تقديم محضر فقدان + صورتين شخصيتين إلى مكتب الأحوال المدنية"
            };
        }

        if (question === "الوثائق الأساسية" && answer === "شهادة الميلاد") {
            return {
                answer: "طريقة الحصول: طلبها من السجل المدني مع إثبات الهوية"
            };
        }

          // --- وثائق الإفراج ---
          if (question === "فئة الوثائق الأساسية" && answer === "وثائق الإفراج والسجل الجنائي") {
            return {
                question: "وثائق الإفراج والسجل الجنائي",
                answers: ["وثيقة الإفراج النهائي", "شهادة السوابق العدلية (عدم محكومية)"]
            };
        }

        if (question === "وثائق الإفراج والسجل الجنائي" && answer === "وثيقة الإفراج النهائي") {
            return {
                answer: "صادرة من السجن عند انتهاء المدة وثبتت انتهاء العقوبة"
            };
        }

        if (question === "وثائق الإفراج والسجل الجنائي" && answer === "شهادة السوابق العدلية (عدم محكومية)") {
            return {
                answer: "تطلب من الأمن الجنائي بعد 3 سنوات من الإفراج (في بعض الجرائم)"
            };
        }
         // ========== فئة الحقوق القانونية ==========
         if (!question) {
            return {
                question: "ما هو الهدف؟",
                answers: ["فئة الحقوق القانونية"]
            };
        }
        
        if (question === "ما هو الهدف؟" && answer === "فئة الحقوق القانونية") {
            return {
                question: "اختر نوع الحق:",
                answers: [
                    "الحق في السكن",
                    "الحق في استعادة الوثائق",
                    "الحق في العمل",
                    "الحق في التعليم والتدريب",
                    "الحق في التقديم على الدعم المالي",
                    "الحق في إزالة القيود الأمنية",
                    "الحق في الحماية من التمييز"
                ]
            };
        }

        // --- الحق في السكن ---
        if (question === "اختر نوع الحق:" && answer === "الحق في السكن") {
            return {
                answer: "لا يمكن طرد المواطن من سكنه بسبب سجل جنائي إلا بحكم قضائي."
            };
        }

        // --- الحق في استعادة الوثائق ---
        if (question === "اختر نوع الحق:" && answer === "الحق في استعادة الوثائق") {
            return {
                answer: "يسمح باستخراج جميع الوثائق الرسمية بعد الإفراج (مادة 56 من قانون الأحوال المدنية)."
            };
        }

        // --- الحق في العمل ---
        if (question === "اختر نوع الحق:" && answer === "الحق في العمل") {
            return {
                answer: "يحظر التمييز ضد المواطن في التوظيف (المادة 30 من قانون العمل السوري)."
            };
        }

        // --- استثناءات العمل ---
        if (question === "الحق في العمل") {
            return {
                answer: "الوظائف الأمنية أو العسكرية تتطلب شهادة ‘عدم محكومية’."
            };
        }

        // --- الحق في التعليم والتدريب ---
        if (question === "اختر نوع الحق:" && answer === "الحق في التعليم والتدريب") {
            return {
                answer: "يسمح بالتسجيل في المراكز التعليمية الحكومية وفق شروط (مادة 18 من الدستور)."
            };
        }

        // --- الحق في التقديم على الدعم المالي ---
        if (question === "اختر نوع الحق:" && answer === "الحق في التقديم على الدعم المالي") {
            return {
                answer: "يمكن التقديم على قروض صغيرة من الصندوق السوري للتنمية بشروط محددة."
            };
        }

        // --- الحق في إزالة القيود الأمنية ---
        if (question === "اختر نوع الحق:" && answer === "الحق في إزالة القيود الأمنية") {
            return {
                answer: "تقديم طلب إلى الأمن الجنائي لإزالة الاسم من القوائم السوداء بعد 3 سنوات."
            };
        }

        // --- الحق في الحماية من التمييز ---
        if (question === "اختر نوع الحق:" && answer === "الحق في الحماية من التمييز") {
            return {
                answer: "يمكن رفع دعوى قضائية عند التعرض للتمييز (المادة 487 من قانون العقوبات)."
            };
        }

        // === معالجة الأخطاء ===
        return { 
            field: "error", 
            message: "إدخال غير صحيح. يرجى اختيار أحد الخيارات المعروضة." 
        };

    } catch (error) {
        return { 
            field: "error", 
            message: "حدث خطأ في النظام. يرجى المحاولة لاحقاً." 
        };
    }
}